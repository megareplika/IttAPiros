    private void elhelyez() {
        int hova = (int) (Math.random() * felso - also + 1) + also;
        for (int i = 0; i < hol.length; i++) {
            hol[i] = false;
        }
        for (boolean b : hol) {
            System.out.println(b);
        }
        for (int i = 0; i < 10; i++) {
            System.out.print("-");
        }
        System.out.println();
        switch (hova) {
            case 0:
                hol[0] = true;
                break;
            case 1:
                hol[1] = true;
                break;
            case 2:
                hol[2] = true;
                break;
        }
        int hely = 0;
        for (int i = 0; i < hol.length; i++) {
            if (hol[i]) {
                hely = i;
            }
        }
        if (new File("./tipp.deb").exists()) {
            this.setTitle("A gogyó a "+hely+1+". pohár alat van");
        }
        for (int i = 0; i < 10; i++) {
            System.out.print("-");
        }
        System.out.println();
    }

    private void talaltE(boolean igaz) {
        if (igaz) {
            lbVisszajelzes.setText("Talált");
            elhelyez();
        } else {
            lbVisszajelzes.setText("Nem talált");
            if (rbUjhely.isSelected()) {
                elhelyez();
            }
        }
    }

